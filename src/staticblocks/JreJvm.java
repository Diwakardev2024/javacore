package staticblocks;

public class JreJvm {
	
	// JDk --> Developing java application
	
	// JRE --> Used for running java programs/application
	
	// JVM --> Provides a Platform Independent execution environment for running your code
	
	/*static:--
	 * static method can not be overridden
	 * can not create abstract method using static keyword
	 * this or super keyword can not be used for static methods 
	 */
	
	/*static block:--
	 * 
	 * A block of code which is used with static keyword is known as static block.
	 * Static block is mainly used for initializing static variables.
	 * we can have multiple static block inside single class.Static block will be executed before 
	 * main method.
	 * 
	 * 
	 */
	
	/*
	 * Static Class:---
	 * When we create a class inside another class it is called a nested class,static class only 
	 * be created as a nested class.Members of the static class can be directly accessed using the outer class.
	 * 1) A static class can not access non-static members of the Outer class.
	 * 2) Static class members can be called with outer class reference.
	 * 3)Static classes are always nested or inner class.
	 * 4)we can use access modifier for static class.
	 * 
	 */
	
	/*
	 * Final keyword :--
	 * 
	 * It is used for restricting  the behavior variable ,method,class,etc.
	 * The final keyword can be used with:
	 * 1) variable
	 * 2) Methods  // can not be overridden
	 * 3) Class    // can not be extended or inherited
	 *  
	 *  NOTE:- If we don't initialize the final variable during declaration it is called blank final variable 
	 *         it can be initialized through constructor.
	 *         
	 */
	

}
